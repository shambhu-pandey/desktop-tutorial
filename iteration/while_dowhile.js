// printing table from 1 to 12 using do while loop 
let j=1;
while(j<=12){
   console.log(`this is the table of ${j}`);
  let k=1;
  while(k<=10){
    console.log(`${j}*${k} = ${j*k}`);
    k++;
  }
  j++;
}



